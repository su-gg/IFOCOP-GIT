/*
Pour la modification des articles, nous allons récupérer les différents contenus d'un article avant de les afficher dans un formulaire où l'utilisateur pourra y apporter toutes les modifications avant de les enregistrer.
*/


/*********************************
*************Exercice*************
*********************************/
/*
------ 1 ------
Dans la liste des articles de l'administation, vous avez prévu un lien pour chaque article permettant de le modifier.

------ 2 ------
Quand l'utilisateur clique sur le lien, vous récupérez l'id de l'article. Vous transformez l'id comme dans l'exercice précédent. Si vous obtenez les différentes informations d'un article, vous affichez le même formulaire que celui de l'enregistrement de l'article. Vous allez donc le modifier en affichant l'information pour chaque champ (mettez des conditions dans votre template).
Vous ajoutez un champ caché pour y stocker l'id de l'article et vous modifiez l'action du formulaire (le traitement se fera sur une autre route).

------ 3 ------
Pour l'enregistrement de l'article modifié, vous commencez par vérifier que l'id de l'article corresponde à l'id d'un article en base de données. Si tel est le cas, vous mettez à jour les informations en base de données.
*/
